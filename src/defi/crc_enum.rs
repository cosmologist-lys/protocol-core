use crate::defi::ProtocolResult;

pub enum CrcType {
    Crc16Ccitt,
    Crc16CcittFalse,
    Crc16Modbus,
    Crc16Xmodem,
    /// 可自定义参数的 CCITT-16 算法
    Crc16CcittCustom {
        poly: u16,
        init: u16,
        xor_out: u16,
        swap_result: bool,
    },
}

pub trait CrcCalculator {
    fn calculate(&self, data: &[u8]) -> ProtocolResult<u16>;
    fn calculate_from_hex(&self, hex: &str) -> ProtocolResult<String>;
}

impl CrcCalculator for CrcType {
    fn calculate_from_hex(&self, hex: &str) -> ProtocolResult<String> {
        let bytes = crate::utils::hex_util::hex_to_bytes(hex)?;
        let r_hex = self.calculate(bytes.as_slice())?;
        Ok(format!("{:04X}", r_hex))
    }
    fn calculate(&self, data: &[u8]) -> ProtocolResult<u16> {
        match self {
            CrcType::Crc16Ccitt => calc_for_crc16ccitt(data),
            CrcType::Crc16CcittFalse => calc_for_crc16ccitt_false(data),
            CrcType::Crc16Xmodem => calc_for_crc16xmodem(data),
            CrcType::Crc16Modbus => calc_for_crcmodbus(data),
            CrcType::Crc16CcittCustom {
                poly,
                init,
                xor_out,
                swap_result,
            } => {
                let table = generate_ccitt_16_table(*poly);
                let mut crc: u16 = *init;
                for &byte in data {
                    let index = (((crc >> 8) ^ (byte as u16)) & 0xFF) as usize;
                    crc = crc.wrapping_shl(8) ^ table[index];
                }
                let mut result = crc ^ *xor_out;
                if *swap_result {
                    result = result.swap_bytes();
                }
                Ok(result)
            }
        }
    }
}

fn generate_ccitt_16_table(poly: u16) -> [u16; 256] {
    let mut table = [0u16; 256];
    let range_inclusive = 0..=255;
    for i in range_inclusive {
        let mut crc: u16 = (i as u16) << 8;

        // Java: for (int j = 0; j < 8; j++)
        for _ in 0..8 {
            if (crc & 0x8000) != 0 {
                crc = crc.wrapping_shl(1) ^ poly;
            } else {
                crc = crc.wrapping_shl(1);
            }
        }
        table[i] = crc;
    }
    table
}

fn calc_for_crcmodbus(bytes: &[u8]) -> ProtocolResult<u16> {
    let mut rchi: u8 = 0xFF;
    let mut rclo: u8 = 0xFF;

    for &byte in bytes {
        let u_index = (rchi ^ byte) as usize;
        rchi = rclo ^ AUCH_CRC_HI[u_index];
        rclo = AUCH_CRC_LO[u_index];
    }
    let raw_crc: u16 = (rchi as u16) << 8 | (rclo as u16);
    Ok(raw_crc.swap_bytes())
}

fn calc_for_crc16ccitt_false(bytes: &[u8]) -> ProtocolResult<u16> {
    let mut crc: u32 = 0xFFFF;
    for &byte in bytes {
        let b = byte as u32;
        crc = ((crc >> 8) & 0xFF) | ((crc & 0xFF) << 8);
        crc ^= b;
        crc ^= (crc & 0xFF) >> 4;
        crc ^= (crc & 0xFFFF) << 12;
        crc ^= (crc & 0xFF) << 5;
    }
    Ok((crc & 0xFFFF) as u16)
}

fn calc_for_crc16ccitt(bytes: &[u8]) -> ProtocolResult<u16> {
    let mut crc_reg: u16 = 0x0000; // 初始值 0x0000
    for &byte in bytes {
        let index = ((crc_reg as u8) ^ byte) as usize;
        crc_reg = CRC_16_CCITT_TABLE[index] ^ (crc_reg >> 8);
    }
    Ok(crc_reg)
}

fn calc_for_crc16xmodem(bytes: &[u8]) -> ProtocolResult<u16> {
    let mut crc: u16 = 0x0000; // 初始值 wCRCin = 0x0000
    const POLY: u16 = 0x1021; // 多项式 wCPoly = 0x1021

    for &byte in bytes {
        // 将 8 位的字节 "混合" 到 16 位 CRC 的高 8 位
        crc ^= (byte as u16) << 8;
        // 执行 8 次移位和异或操作
        for _ in 0..8 {
            if (crc & 0x8000) != 0 {
                // 如果 MSB 是 1, 左移一位并与多项式异或
                crc = (crc << 1) ^ POLY;
            } else {
                // 如果 MSB 是 0, 只左移一位
                crc <<= 1;
            }
        }
    }
    Ok(crc)
}

